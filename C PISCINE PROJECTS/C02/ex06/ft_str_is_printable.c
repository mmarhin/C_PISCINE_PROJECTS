int	ft_str_is_printable(char *str)
{
	int	ok;

	ok = 1;
	while ((ok == 1) && *str)
	{
		ok = 0;
		if (*str >= 32 && *str <= 126)
		{
			ok = 1;
		}
		str++;
	}
	return (ok);
}
