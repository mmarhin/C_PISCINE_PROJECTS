#include <unistd.h>

void	ft_is_negative(int n)
{
	char	valor;

	if (n < 0)
	{
		valor = 78;
		write (1, &valor, 1);
	}
	else
	{
		valor = 80;
		write (1, &valor, 1);
	}
}
